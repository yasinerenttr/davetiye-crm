function toHref(platform, url = '') {
  let value = String(url).trim()
  if (!value) return ''
  if (value.startsWith('@')) value = value.slice(1)
  if (value.startsWith('http://') || value.startsWith('https://')) return value

  if (platform === 'instagram') {
    if (!value.includes('instagram.com')) return `https://instagram.com/${value}`
  }
  if (platform === 'tiktok') {
    if (!value.includes('tiktok.com')) return `https://tiktok.com/@${value}`
  }
  if (platform === 'facebook') {
    if (!value.includes('facebook.com')) return `https://facebook.com/${value}`
  }
  if (platform === 'whatsapp') {
    const phone = value.replace(/\D/g, '')
    if (phone) return `https://wa.me/${phone}`
  }

  return `https://${value}`
}

